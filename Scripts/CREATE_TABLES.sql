--this script creates the database tables and populates them with sample data

CREATE TABLE COUNTY
(COUNTY_ID VARCHAR(10) PRIMARY KEY,
COUNTY_NAME VARCHAR(45),
COUNTY_TAX DECIMAL(6,4) );

CREATE TABLE MUNICIPALITY
(MUNICIPALITY_ID VARCHAR(10) PRIMARY KEY,
MUNICIPALITY_NAME VARCHAR(45),
MUNICIPALITY_TAX DECIMAL (6,4) );

CREATE TABLE SCHOOL_DISTRICT
(SCHOOL_DISTRICT_ID VARCHAR(10) PRIMARY KEY,
SCHOOL_DISTRICT_NAME VARCHAR(45),
SCHOOL_DISTRICT_TAX DECIMAL(6,4) );

CREATE TABLE LISTING_AGENT
(LISTING_AGENT_ID VARCHAR(5) PRIMARY KEY,
FIRSTNAME VARCHAR(45),
LASTNAME VARCHAR(45),
AGENCY VARCHAR(45),
PHONE VARCHAR(10),
EMAIL VARCHAR(45) );

CREATE TABLE PROPERTY
(MLS_NUM VARCHAR(10) PRIMARY KEY,
LISTED_PRICE DECIMAL(6,0),
ASSESSMENT DECIMAL(6,0),
ADDRESS VARCHAR(60),
CITY VARCHAR(30),
COUNTY_ID VARCHAR(10) FOREIGN KEY REFERENCES COUNTY,
MUNICIPALITY_ID VARCHAR(10) FOREIGN KEY REFERENCES MUNICIPALITY,
SCHOOL_DISTRICT_ID VARCHAR(10) FOREIGN KEY REFERENCES SCHOOL_DISTRICT,
BEDRMS INT,
BATHRMS DECIMAL(2,1),
SQR_FT INT,
DATE_ON_MKT DATE,
LISTING_AGENT_ID VARCHAR(5) FOREIGN KEY REFERENCES LISTING_AGENT);


CREATE TABLE APPOINTMENT
(APPT_ID INT IDENTITY(1,1) PRIMARY KEY,
APPT_DATE DATE,
APPT_TIME TIME(0),
MLS_NUM VARCHAR(10) FOREIGN KEY REFERENCES PROPERTY,
DURATION INT,
TYPE VARCHAR(45) );

